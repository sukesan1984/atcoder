#include <stdio.h>

int main()
{
	unsigned long long N;
	unsigned long long ans;

	scanf("%llu", &N);

	if (N < 1000)
	{
		ans = 0;
	}
	else if (N < 1000000)
	{
		ans = (N - 999);
	}
	else if (N < 1000000000)
	{
		ans = (999999 - 999) + (N - 999999) * 2;
	}
	else if (N < 1000000000000)
	{
		ans = (999999 - 999) + (999999999 - 999999) * 2 + (N - 999999999) * 3;
	}
	else if (N < 1000000000000000)
	{
		ans = (999999 - 999) + (999999999 - 999999) * 2 + (999999999999 - 999999999) * 3 + (N - 999999999999) * 4;
	}
	else
	{
		ans = (999999 - 999) + (999999999 - 999999) * 2 + (999999999999 - 999999999) * 3 + (999999999999999 - 999999999999) * 4 + 5;

	}
	printf("%llu\n", ans);

	return (0);
}
